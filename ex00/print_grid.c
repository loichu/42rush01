#include <unistd.h>

void	print_grid(int **grid)
{
	int		i;
	int		j;
	char	nb;

	i = 1;
	while (i < 5)
	{
		j = 1;
		while (j < 5)
		{
			nb = '0' + grid[i][j];
			write(1, &nb, 1);
			if (j < 4)
				write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
